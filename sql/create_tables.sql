    win INTEGER,
    offensive_plays INTEGER,
    off_epa_per_play DOUBLE PRECISION,
    pass_plays INTEGER,
    rush_plays INTEGER,
    interceptions_thrown INTEGER,
    fumbles_lost INTEGER,
    pass_rate DOUBLE PRECISION,
    turnovers INTEGER
);

