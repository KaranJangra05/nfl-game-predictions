DROP TABLE IF EXISTS model_features;
DROP TABLE IF EXISTS team_games_with_rolling;
DROP TABLE IF EXISTS team_games_with_stats;
DROP TABLE IF EXISTS team_games_base;
DROP TABLE IF EXISTS games;

CREATE TABLE games (
    game_id TEXT PRIMARY KEY,
    season INTEGER,
    week INTEGER,
    gameday DATE,
    away_team TEXT,
    away_score INTEGER,
    home_team TEXT,
    home_score INTEGER,
    away_rest INTEGER,
    home_rest INTEGER
);

CREATE TABLE team_games_base (
    game_id TEXT,
    season INTEGER,
    week INTEGER,
    gameday DATE,
    team TEXT,
    opponent TEXT,
    points_for INTEGER,
    points_against INTEGER,
    rest_days INTEGER,
    is_home INTEGER,
    win INTEGER
);

CREATE TABLE team_games_with_stats (
